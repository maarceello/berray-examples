package com.berray.examples.pong.data;

/** Global game data. */
public class GameData {
  private int score = 0;
  private int speed = 480;

  public int getScore() {
    return score;
  }

  public void setScore(int score) {
    this.score = score;
  }

  public int getSpeed() {
    return speed;
  }

  public void setSpeed(int speed) {
    this.speed = speed;
  }

}
